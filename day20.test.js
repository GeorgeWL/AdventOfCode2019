function createMaze(mazeString) {
	const startPoint = { row: 0, col: 0 };
	const endPoint = { row: 0, col: 0 };
	const maze2dArray = create2dArray(mazeString);
	return { startPoint, endPoint, maze2dArray };
}
function create2dArray(mazeString) {
	return mazeString.split('\n').map(row => row.split(''));
}

